int somma (int n1,int n2)
{
    int risultato = n1 + n2;
    return risultato;
}

int differenza (int n1,int n2)
{
    int risultato = n1 - n2;
    return risultato;
}

int divisione (int n1,int n2)
{
    int risultato = n1 / n2;
    return risultato;
}

int moltiplicazione (int n1,int n2)
{
    int risultato = n1 * n2;
    return risultato;
}